    private void doCreateRoom() {
        if (jTextField1.getText() == null || jTextField1.getText().isEmpty()) {
            return;
        }
        if (jTextField2.getText() == null || jTextField2.getText().isEmpty()) {
            return;
        }
        JOptionPane.showMessageDialog(this, "Waiting for another player");
        xo.createRoom(jTextField1.getText(), jTextField2.getText());
        dispose();
    }

